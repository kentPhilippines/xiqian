package com.how2java.tmall.pojo;

import java.util.Date;

public class PayEntity {
	private String orderNo;
	private Date orderTime ; //订单时间
	private String orderTitle;//订单标题
	private String orderbody;//订单描述
	private String amount;//交易金额
	private String accNo;//交易银行卡
	
	public PayEntity(String orderNo, Date orderTime, String orderTitle, String orderbody, String amount,
			String accNo) {
		super();
		this.orderNo = orderNo;
		this.orderTime = orderTime;
		this.orderTitle = orderTitle;
		this.orderbody = orderbody;
		this.amount = amount;
		this.accNo = accNo;
	}
	public String getOrderNo() {
		return orderNo;
	}
	public void setOrderNo(String orderNo) {
		this.orderNo = orderNo;
	}
	public Date getOrderTime() {
		return orderTime;
	}
	public void setOrderTime(Date orderTime) {
		this.orderTime = orderTime;
	}
	public String getOrderTitle() {
		return orderTitle;
	}
	public void setOrderTitle(String orderTitle) {
		this.orderTitle = orderTitle;
	}
	public String getOrderbody() {
		return orderbody;
	}
	public void setOrderbody(String orderbody) {
		this.orderbody = orderbody;
	}
	public String getAmount() {
		return amount;
	}
	public void setAmount(String amount) {
		this.amount = amount;
	}
	public String getAccNo() {
		return accNo;
	}
	public void setAccNo(String accNo) {
		this.accNo = accNo;
	}
	public PayEntity() {
		super();
		// TODO Auto-generated constructor stub
	}
	@Override
	public String toString() {
		return "PayEntity [orderTime=" + orderTime + ", orderTitle=" + orderTitle + ", orderbody=" + orderbody
				+ ", amount=" + amount + ", accNo=" + accNo + "]";
	}
	public PayEntity(Date orderTime, String orderTitle, String orderbody, String amount, String accNo) {
		super();
		this.orderTime = orderTime;
		this.orderTitle = orderTitle;
		this.orderbody = orderbody;
		this.amount = amount;
		this.accNo = accNo;
	}
	
}
